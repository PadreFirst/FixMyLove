def build_user_data_block(user: dict[str, Any]) -> str:
    name = user.get("name") or "не указано"
    age = user.get("age") or "не указано"
    partner_name = user.get("partner_name") or "не указано"
    partner_age = user.get("partner_age") or "не указано"
    status = user.get("relationship_status") or "не указано"
    note = user.get("relationship_note") or ""
    attachment = user.get("attachment_style") or "не определён"
    facts = user.get("important_facts", [])

    block = f"""[ДАННЫЕ ПОЛЬЗОВАТЕЛЯ]
Имя: {name}
Возраст: {age}
Партнёр: {partner_name}, {partner_age} лет
Статус: {status}{(' — ' + note) if note else ''}
Тип привязанности: {attachment}"""

    if facts:
        block += "\nВажные факты: " + "; ".join(facts[-10:])

    return block


def build_session_params_block(dynamic: dict[str, Any]) -> str:
    if not dynamic.get("session_open"):
        return "[ПАРАМЕТРЫ ТЕКУЩЕЙ СЕССИИ]\nСессия не открыта."

    return f"""[ПАРАМЕТРЫ ТЕКУЩЕЙ СЕССИИ]
Тип: {dynamic.get('situation_type', 'не определён')}
Паттерн: {dynamic.get('dominant_pattern', 'не определён')}
Методика: {dynamic.get('methodology', 'не определена')}
Фаза: {dynamic.get('current_phase', 'не определена')}
Тон: {dynamic.get('tone', 'не определён')}
Цель пользователя: {dynamic.get('user_goal', 'не определена')}"""


