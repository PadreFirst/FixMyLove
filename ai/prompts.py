def build_sliding_window_block(window: list[dict[str, str]]) -> str:
    if not window:
        return ""
    lines = []
    for msg in window:
        role = "Пользователь" if msg.get("role") == "user" else "Бот"
        lines.append(f"{role}: {msg.get('text', '')}")
    return "[ИСТОРИЯ ДИАЛОГА]\n" + "\n".join(lines)


def build_summaries_block(summaries: list[dict[str, Any]]) -> str:
    if not summaries:
        return ""
    lines = ["[ПРЕДЫДУЩИЕ СЕССИИ]"]
    for s in summaries:
        lines.append(
            f"- {s.get('date', '?')}: {s.get('user_request', '?')} | "
            f"Паттерн: {s.get('dominant_pattern', '?')} | "
            f"Действие: {s.get('user_action', '?')} | "
            f"Настроение: {s.get('mood_in', '?')}→{s.get('mood_out', '?')}"
        )
    return "\n".join(lines)


def build_full_system_prompt(
    user: dict[str, Any],
    dynamic: dict[str, Any],
    summaries: list[dict[str, Any]] | None = None,
    window: list[dict[str, str]] | None = None,
) -> str:
    user_block = build_user_data_block(user)
    session_block = build_session_params_block(dynamic)
    pattern = dynamic.get("dominant_pattern", "undefined")
    meth_block = build_methodology_block(pattern)

    extra = ""
    if summaries:
        extra += "\n\n" + build_summaries_block(summaries)
    if window:
        extra += "\n\n" + build_sliding_window_block(window)

    return SYSTEM_PROMPT_TEMPLATE.format(
        user_data_block=user_block,
        session_params_block=session_block,
        methodology_block=meth_block,
    ) + extra


